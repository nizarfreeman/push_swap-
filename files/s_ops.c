void	sa_(int *arr)
{
	int	tmp;

	tmp = *arr;
	*arr = *(arr + 1);
	*(arr + 1) = tmp;
}

void	sb_(int *arr)
{
	int	tmp;

	tmp = *arr;
	*arr = *(arr + 1);
	*(arr + 1) = tmp;
}

void	ss_(int *a, int *b)
{
	sa_(a);
	sb_(b);
}
